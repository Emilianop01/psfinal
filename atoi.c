#include "push_swap.h"

long	ftatoi(const char *num)
{
	int		i[2];
	long	segno;
	long	d;

	segno = 1;
	i[0] = 0;
	d = 0;
	i[1] = 0;
	while (num[i[0]] == ' ')
		i[0]++;
	if (num[i[0]] == '+' || num[i[0]] == '-')
	{
		if (num[i[0]] == '-')
			segno = -1;
		i[0]++;
	}
	while (num[i[0]])
	{
		d = d * 10 + num[i[0]] - 48;
		i[0]++;
		i[1]++;
		if (i[1] >= 11)
			return (22222222222);
	}
	return (d * segno);
}
